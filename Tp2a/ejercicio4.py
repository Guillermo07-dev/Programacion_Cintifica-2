#Contenido del punto : Diseña un programa que tras asignar dos listas a sendas variables nos diga si la primera es menor que la segunda. 
#Respuesta:

# Asignación de dos listas
lista1 = [1, 2, 3]
lista2 = [1, 2, 4]

# Comparación de listas
if lista1 < lista2:
    print("La primera lista es menor que la segunda.")
elif lista1 > lista2:
    print("La primera lista es mayor que la segunda.")
else:
    print("Ambas listas son iguales.")
